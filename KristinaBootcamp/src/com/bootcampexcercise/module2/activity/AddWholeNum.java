package com.bootcampexcercise.module2.activity;

public class AddWholeNum {
    public static void  main(String args[]) {
        int sum = 0;
        for (int i = 50; i<=100; i++) {
            sum = sum + i;
        }
        System.out.println("Sum of numbers between 50 and 100 is " + sum);
    }
}
