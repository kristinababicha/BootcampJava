public class OperatorActivity {
    public static void main(String args[]) {
        int x = 5;
        int y = --x;
        System.out.println("Value of y is " + y + " Value of x is " + x);
        int z = x--;
        System.out.println("Value of z is " + z + "Value of x is " + x);
    }

}


